    public BSDate toBS(LocalDate adDate) {
        long totalDays = ChronoUnit.DAYS.between(LocalDate.of(AD_BASE_YEAR, AD_BASE_MONTH, AD_BASE_DAY), adDate);
        if (totalDays < 0) {
            throw new IllegalArgumentException("Date out of supported range: " + adDate);
        }

        int year = BS_BASE_YEAR;
        int month = 0;

        while (true) {
            int yearIndex = year - BS_BASE_YEAR;
            if (yearIndex >= BS_MONTH_DAYS.length) {
                throw new IllegalArgumentException("Date out of supported range: " + adDate);
            }
            int daysInYear = 0;
            for (int m = 0; m < 12; m++) {
                daysInYear += BS_MONTH_DAYS[yearIndex][m];
            }
            if (totalDays < daysInYear) {
                for (int m = 0; m < 12; m++) {
                    int daysInMonth = BS_MONTH_DAYS[yearIndex][m];
                    if (totalDays < daysInMonth) {
                        month = m + 1;
                        return BSDate.of(year, month, (int) totalDays + 1);
                    }
                    totalDays -= daysInMonth;
                }
            }
            totalDays -= daysInYear;
            year++;
        }
    }

    public LocalDate toAD(BSDate bsDate) {
        int yearIndex = bsDate.year() - BS_BASE_YEAR;
        if (yearIndex < 0 || yearIndex >= BS_MONTH_DAYS.length) {
            throw new IllegalArgumentException("BS year out of supported range: " + bsDate.year());
        }
        if (bsDate.month() < 1 || bsDate.month() > 12) {
            throw new IllegalArgumentException("Invalid BS month: " + bsDate.month());
        }

        long totalDays = 0;
        for (int y = 0; y < yearIndex; y++) {
            for (int m = 0; m < 12; m++) {
                totalDays += BS_MONTH_DAYS[y][m];
            }
        }
        for (int m = 0; m < bsDate.month() - 1; m++) {
            totalDays += BS_MONTH_DAYS[yearIndex][m];
        }
        totalDays += bsDate.day() - 1;

        return LocalDate.of(AD_BASE_YEAR, AD_BASE_MONTH, AD_BASE_DAY).plusDays(totalDays);
    }

    public String formatBS(BSDate bsDate) {
        String[] monthNames = {
            "Baisakh", "Jestha", "Ashadh", "Shrawan", "Bhadra", "Ashwin",
            "Kartik", "Mangsir", "Poush", "Magh", "Falgun", "Chaitra"
        };
        return monthNames[bsDate.month() - 1] + " " + bsDate.day() + ", " + bsDate.year() + " BS";
    }
